#include<bits/stdc++.h>
using namespace std;

#define nl cout<<"\n";
#define sf cin>>
#define pf cout<<
#define ll long long

void fun()
{
    int arr[6]={-1,0,0,0,0,0};
    for(int i=0; i<4; i++)
    {
        int variable;
        sf variable;
        arr[variable]=variable;
    }
    for(int i=1; i<6; i++)
    {
        if(arr[i]==0)
        {
            pf i;
            nl
            break;
        }
    }
}
int main()
{
  ios::sync_with_stdio(false);
    cin.tie(0);

    fun();

    return 0;
}
